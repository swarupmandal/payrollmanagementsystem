package org.appsquad.bean;

public class EsiReportBean {

	private String lvYr;
	private String salaryMonth;
	private int salaryMonthId;
	private int companyId;
	private int unitId;
	private int unitDesignationId;
	
	

	public String getLvYr() {
		return lvYr;
	}

	public void setLvYr(String lvYr) {
		this.lvYr = lvYr;
	}

	public String getSalaryMonth() {
		return salaryMonth;
	}

	public void setSalaryMonth(String salaryMonth) {
		this.salaryMonth = salaryMonth;
	}

	public int getSalaryMonthId() {
		return salaryMonthId;
	}

	public void setSalaryMonthId(int salaryMonthId) {
		this.salaryMonthId = salaryMonthId;
	}

	public int getCompanyId() {
		return companyId;
	}

	public void setCompanyId(int companyId) {
		this.companyId = companyId;
	}

	public int getUnitId() {
		return unitId;
	}

	public void setUnitId(int unitId) {
		this.unitId = unitId;
	}

	public int getUnitDesignationId() {
		return unitDesignationId;
	}

	public void setUnitDesignationId(int unitDesignationId) {
		this.unitDesignationId = unitDesignationId;
	}

	
}
